let fruit = 'apple';

switch(fruit) {
  case 'apple':
    console.log(0.25);
    break;
  case 'banana':
    console.log(0.15);
    break;
  case 'pear':
    console.log(0.35);
    break;
  default:
    console.log(0);
}

